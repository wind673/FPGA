unsigned int Audio_Main(void)		
{  	 
	AD9851_FM(ADC_Result_Get(2,5)*3.3f/4096.0f);  
	
	return 0;																	
}		

unsigned int Audio_Start(void)		
{
	static u8 first = 0;
	if(first == 0)
	{
		first = 1;     
		AD9851_Init(); 
		ADC2_Init();
	}
	
	return 0;
}
 
